void printMapWithPlayer(int playerX, int playerY) {
    for (int y = 0; y < mapHeight; y++) {
        for (int x = 0; x < mapWidth; x++) {
            if (x == playerX && y == playerY)
                std::cout << 'P';
            else
                std::cout << map[y * mapWidth + x];
        }
        std::cout << "\n";
    }
}
